function waitForRedisConnection(timeoutMs = 10000) {
  return new Promise((resolve, reject) => {
    const timeout = setTimeout(() => {
      reject(new Error(`Redis connection timeout after ${timeoutMs}ms`));
    }, timeoutMs);

    if (redis.status === 'ready') {
      clearTimeout(timeout);
      resolve();
      return;
    }

    redis.once('ready', () => {
      clearTimeout(timeout);
      resolve();
    });

    redis.once('error', (err) => {
      clearTimeout(timeout);
      reject(err);
    });
  });
}

async function gracefulShutdown(worker) {
  console.log('üõë Starting graceful worker shutdown...');
  
  try {
    if (worker) {
      await worker.close();
      console.log('‚úÖ Worker closed');
    }

    await redis.quit();
    console.log('‚úÖ Redis connection closed');
    
    console.log('‚úÖ Worker process shutdown completed');
    process.exit(0);
  } catch (error) {
    console.error('‚ùå Error during worker shutdown:', error.message);
    process.exit(1);
  }
}

